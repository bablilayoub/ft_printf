#include "ft_printf.h"

char	is_rule(char const *str, int *i)
{
	int	temp;

	temp = 0;
	if (str[*i] == '%' && (str[*i + 1] == 'c' 
			|| str[*i + 1] == 's' 
			|| str[*i + 1] == 'p' 
			|| str[*i + 1] == 'd'
			|| str[*i + 1] == 'i'
			|| str[*i + 1] == '%'
			|| str[*i + 1] == 'u'
			|| str[*i + 1] == 'x'
			|| str[*i + 1] == 'X'
		))
	{
		temp = *i + 1;
		(*i)++;
		return (str[temp]);
	}
	return ('\0');
}
